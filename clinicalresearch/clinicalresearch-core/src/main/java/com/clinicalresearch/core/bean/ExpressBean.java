package com.clinicalresearch.core.bean;

public class ExpressBean {
	
	private Object expressions;
	
	private String page;
	
	private String size;
	
	private Object resultField;

	public Object getExpressions() {
		return expressions;
	}

	public void setExpressions(Object expressions) {
		this.expressions = expressions;
	}

	

	public String getPage() {
		return page;
	}

	public void setPage(String page) {
		this.page = page;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public Object getResultField() {
		return resultField;
	}

	public void setResultField(Object resultField) {
		this.resultField = resultField;
	}
	
	
}
